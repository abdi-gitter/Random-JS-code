//while loop in js
let apples = 5
let oranges = 10

// as long as this condition is true, we loop:
while (apples < oranges) {
    console.log("apples are less than oranges")
    console.log("Apples: " + apples)
    console.log("Oranges: " + oranges)
    console.log('ADDING APPLE!')
    console.log(('--------------------------------'))

    // WE HAVE TO CHANGE THE VALUE OF APPLES TO EVENTUALLY GET OUT OF THE LOOP
    apples = apples + 1
}

console.log("apples are equal to oranges")
